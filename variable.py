# Proses memasukkan data ke dalam variabel
nama = "Gilang"
# Proses mencetak variabel
print(nama)
# Nilai dan tipe data dalam variabel dapat diubah
umur = 16               # Nilai awal
print(umur)             # Mencetak nilai umur
print(type(umur))      # Mengecek tipe data umur
umur = "enam belas" # Nilai setelah diubah
print(umur)             # Mencetak nilai umur
print(type(umur))      # Mengecek tipe data umur
# Menggabungkan nama depan dan nama belakang
namaDepan = "Rambu"
namaBelakang = "Rabbani"
nama = namaDepan + " " + namaBelakang
umur = 17
hobi = "olahraga"
print("Biodata:\n", nama, "\nUmur:", umur, "\nHobi:", hobi)
